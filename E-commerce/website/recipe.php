			<h2>Directions</h2>
			<ol type="1">
				<li>Place the peppers on a baking sheet with the 
				cut side facing down.</li>
				<li>Place the baking sheet on the top shelf in the oven 
				and broil until the outer layer of the skin has 
				blackened, about 8-14 minutes.</li>
				<li>Place the peppers in a zip-lock bag or other sealable 
				container, seal and let them cool until you can handle 
				them, about 20 minutes.</li>
				<li>Remove the skins from the peppers. The skins should 
				easily "pinch" off.</li>
				<li>Butter the outside of each slice of bread and spread the 
				cream cheese on the inside. Sprinkle half of the cheese on 
				the cream cheese of one slice of bread, top with 
				the jalapenos, crumbled tortilla chips, the remaining 
				cheese and finally the other slice of bread.</li>
				<li>Heat a non-stick pan over medium heat.</li>
				<li>Add the sandwich and grill until golden brown and the 
				cheese has melted, about 2-4 minutes per side.</li>
